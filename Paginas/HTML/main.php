            <h1>Introdução</h1>
            
            Os icebergs da internet são uma metáfora para a vasta quantidade de informações que estão disponíveis na internet, mas que não são facilmente acessíveis ou visíveis. Eles são chamados de icebergs porque, assim como um iceberg, a maior parte do conteúdo está escondida abaixo da superfície.
            Apenas uma pequena parte é visível para a maioria dos usuários.
Aqui estão algumas curiosidades sobre os icebergs da internet:
<p>A Deep Web é uma parte da internet que não pode ser acessada por meio de motores de busca convencionais, como o Google. 
Ela é composta por sites que não são indexados pelos motores de busca e requerem um software especializado para acessá-los.
A <strong>Dark Web</strong> é uma parte da <strong>Deep Web</strong> que é usada principalmente para atividades ilegais, como tráfico de drogas. 
Ela é acessada por meio de redes privadas virtuais <strong>(VPNs)</strong> e requer um software especializado para acessá-la.</p>
<p>A maior parte da internet é composta por conteúdo dinâmico, como páginas da web que são geradas dinamicamente a partir de bancos de dados.
Isso significa que o conteúdo pode mudar com frequência e pode ser diferente para cada usuário.</p>
<p>A internet é tão grande que ninguém sabe <strong>quantas páginas da web existem.</strong></p> Alguns estimam que existam mais de 1 trilhão de páginas da web.
A internet é alimentada por cabos submarinos que se estendem por todo o mundo. Esses cabos transportam dados de um continente para outro e são essenciais para manter a internet funcionando.
