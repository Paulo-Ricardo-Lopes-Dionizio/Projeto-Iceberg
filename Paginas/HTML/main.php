            <li>Aguas Medias: Local aonde vai ser menos facil de achar, muito dos casos são documentos</li>
            <li>Aguas Profundas: Os dados são aqueles que possuem um grau de privacidade bem rigida, sendo coisas polemicas ou absurdas para o <strong>iceberg</strong></li>
            </ol> 
